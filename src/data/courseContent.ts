export const getWeek = (weekNumber: number): CourseWeek | undefined => {
  return courseContent.find(week => week.weekNumber === weekNumber);
};

export const getLesson = (weekNumber: number, dayNumber: number): Lesson | undefined => {
  const week = getWeek(weekNumber);
  return week?.lessons.find(lesson => lesson.dayNumber === dayNumber);
};
